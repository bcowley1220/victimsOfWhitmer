</section>

<section>
    <h2 class="sectionTitle">MY SON’S EDUCATION</h2>
    <div class="pWrapper">
        <p>
            My son was a sophomore in college when Governor Whitmer ordered the first shutdown in the spring of 2020. As a Communications major in Digital Media and Film and Video, he was doing well and thriving in that environment.  But then the shutdown happened. He was forced off campus and sent home. Gone was his ability to work on campus, to help offset the cost of his education. Gone was the reality of being able to take a job at home — not knowing how long he would be available, or when he would head back to school. And gone was any ability to “stay the course” in finishing school on time.
        </p>
        <p>
            It was frustrating to watch my son emotionally crumble as his learning process faded. He was supposed to be getting actual live experience with high-tech equipment and shooting films that would be competing against other Christian school programs. The schools were figuring out how to make it work — but Governor Whitmer’s decrees were putting roadblocks up at every turn. How does a Film and Video major get hands-on learning experience without training with the actual equipment? You can only make so many videos from your home!
        </p>
        <p>
            My son, and thousands of other students, was not receiving the best education this season in life is meant to give him. Thanks to Governor Whitmer and her “one size fits all” mandates, my son is not going to graduate prepared as he should be for the workforce.  But what he was gaining was a larger school bill: the longer it takes to complete the education, the higher the cost of education. He must then determine: does he bail on college? Give up and just wade into the workforce? Does he try to make up for lost experiences by adding more semesters to his load and therefore a larger college debt?
        </p>
    </div>
</section>

<section>
    <h3 class="sectionTitle">WE ADAPTED – WHY COULDN’T SHE?</h3>
    <div class="pWrapper">
        <p>
            We were okay to be patient for a couple of weeks as we understood that adjustments had to be made given the circumstances. But adjustments by Governor Whitmer for our state and our schools were never made; the lockdown just went on and on.  The University and its students had to scramble to adapt and make ongoing education possible. Though it was limited and restrained, they adapted and did as best they could while still allowing students to go on living.
        </p>
        <p>
            Why couldn’t Governor Whitmer adapt, be creative, find ways for life to go on?  Governor Whitmer stifled life and learning, competition, experiences, celebrations, memories, and hope for the future from our youth. And it grieves me. It is as though she only has one way to defeat a virus: shut down everyone, and everything, without bothering to find a way to make life livable. Shut things down, no matter how long, no matter what the cost, no matter who is impacted by it.
        </p>
    </div>
</section>

<section>
    <h4 class="sectionTitle">WHAT EXAMPLE DID SHE GIVE THE NEXT GENERATION?</h4>
    <div class="pWrapper">
        <p>
            As a parent, I was waiting, watching, and hoping for a strong, positive leader to stand behind and guide us through the storm. That light never came. Instead, dull and lingering darkness spread across the state.  My son is only one of the millions of students whose education has been stifled, stomped on, and crushed thanks to our Governor.
        </p>
        <p>
            Our youth deserve a better education experience — yes, even in the midst of unusual circumstances. Our students should have been able to experience leadership in our state’s top leader: encounter a challenge? Figure out how to get around it and keep on living!  I do not blame COVID19 for the crisis in my son’s education experience. I blame Governor Whitmer. She could have — and should have — done better.
        </p>
    </div>
</section>

