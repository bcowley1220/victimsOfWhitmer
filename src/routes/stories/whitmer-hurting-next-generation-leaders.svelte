<h1 class="pageTitle">Governor Whitmer Hurting the Next Generation of Leaders</h1>

<section>
    <p>
        Uncertainty is a normal part of life. When one chooses to run for office, or step into a position of leadership, an aspiring leader must be prepared for problem-solving, flexibility, and making fast adjustments. Good leaders adapt.  Over the past year, Michigan citizens have concluded that Governor Whitmer does not know how to adapt and our state is hurting for it. And specifically to my story: the next generation of leaders is being squelched and suffocated due to her lack of problem-solving skills.
    </p>
