<h1 class="pageTitle">How Whitmer Squeezed Life Out of A Good Man</h1>

<section>
    <p>
        My father, Tom Rhoda, was 87 years old and living in his home during the winter/early spring of 2020. He enjoyed being outdoors in warmer weather, and he especially enjoyed walking and working in his yard. Winters were challenging but he depended on fellowship at church, eating out at restaurants, the Senior Neighbor center, and walking in the mall.  Then Governor Gretchen Whitmer issued her lockdown orders. All of a sudden, Dad was essentially a prisoner in his own house.
    </p>
    <p>
        Dad was terribly lonely. He could not walk in a safe indoor place. He could not get balanced meals. He could not enjoy a healthy lifestyle of community, worship, and fellowship. He was in isolation, sedentary, and in solitary confinement (or so it seemed to him).  It is profound how quickly he spiraled downward. Almost before our eyes, he atrophied physically, mentally and socially. During this time, his health began to rapidly deteriorate.
    </p>
    <div class="imgWrapper"></div>
    <p>
        We thought there was a breakthrough when the warmer weather came. He was finally able to be outdoors and he was eager to begin walking outside again. However, on his first attempt, he fell, hurting his head and back. This began a long, steady decline from which Dad never recovered.  Our father passed away in September 2020.  I am so grateful for his long life. However, his final days were shortened and degraded by Governor Whitmer’s COVID-19 illegal and cruel lockdowns. They were oppressive, destructive and deadly. My dad never deserved the kind of “end of life days” she forced upon him.  My heart hurts. Yet I am glad to be able to have more people know the truth. This Governor is a disgrace. I will do all I can to make sure she never has a second term.
    </p>
    <p>Dad, rest in peace.</p>
</section>

