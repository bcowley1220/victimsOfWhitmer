<h1 class="pageTitle">One Set of Rules for Us, Another Set for Whitmer</h1>

<section>
    <p>
        At the beginning of 2020, my family was hopeful for a good year. Like everyone else, we had no idea how awful the year would become.  My mother had been in a nursing home for approximately two years. At her age, and with some previous health challenges, she had trouble talking. But she could still get around and loved to walk.
    </p>
    <div class="imgWrapper"></div>
    <p>
        My father visited mom every day, and my siblings and I visited her often as well. Her activity and her frequent interactions with us gave her spunk and life.  Then Michigan’s COVID19 shutdown hit and Governor Whitmer’s abuse of power began.
    </p>
    <p>
        Because of Whitmer’s orders, none of us could visit my mother anymore, and we feared the worst. The very thing that had given mom life — frequent interactions, regular activity — was taken away from her. And in her condition and at her age, mom couldn’t understand why we all stopped coming to see her.  My mother passed away 11 days after the shutdown began, alone, without family by her side. 
    </p>
    <p>
        The Governor’s shutdown didn’t have to be so complete. We could have washed our hands and worn masks. We could have visited on the other side of a plastic divider. Surely Governor Whitmer and her team could have figured out a way to make it work. But months and months went by and people were still dying alone because Whitmer and the bureaucrats cared more about their rules than the human beings affected by them.
    </p>
    <p>
        Our family was unable to give mom a big, proper funeral which she had wanted. We were unable to properly say goodbye. We were unable to adequately grieve.
    </p>
    <p>
        All of this was extremely upsetting but what made things even worse was what we were witnessing of Michigan’s top leader. <span><strong>My family and I got to watch Governor Whitmer on TV rubbing elbows and not social distancing with the Detroit Mayor and the George Floyd protestors.</strong></span>
    </p>
    <p>
        You see, there was one set of rules for us and people like my mother. And there was a different set of rules for people like Gretchen Whitmer. There was one set of rules for people dying alone and a different set of rules for people protesting in the streets.
    </p>
    <p>
        <strong>
            Of course, it was Gretchen Whitmer writing the rules.
        </strong>
    </p>
    <p>
        In honor of my mother and other Seniors who are still living and enjoying a quality life, I will do all that I can to make sure this Governor never has another term to rule by decree. Michigan families deserve better.
    </p>
</section>

