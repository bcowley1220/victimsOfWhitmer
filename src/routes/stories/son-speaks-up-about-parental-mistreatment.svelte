<h1 class="pageTitle">A Son Speaks Up Against Mistreatment of Parents by Governor Whitmer</h1>

<p>A wise person once said, “do unto others what you would have them do unto you.” I cannot in a million years imagine Governor Whitmer ever treating her parents how she has treated mine in the past year.</p>


<section>
    <h2 class="sectionTitle">MEET MOM AND DAD</h2>
    <div class="imgWrapper"></div>
    <div class="pWrapper">
        <p>
            Jack and Marilyn Wheeler are my parents. Dad quit high school to voluntarily join the Marine Corp during the Korean War. He was a proud Marine and learned his trade of truck driving in the military. Upon his return home, Dad joined the Teamsters. He supported Mom, my two siblings, and myself on a trucker’s wage, and when times got tight, my mother would go to work part-time to help make ends meet. When Dad retired, he enjoyed years of activity, frequently working out, managing the lawn, and taking daily walks in the neighborhood. He was always mingling with others, and he always had a smile on his face. He was known and liked by the community, and both my parents made many friendships.</p>
            <p>Dad was always physically and mentally sharp. When my mom’s mental health began to fade, it was Dad that took care of her and kept their world running smoothly—cleaning, doing laundry, getting or making food, and managing all the trips to the doctors. It was not long before the late stages of dementia took hold of her, but Dad continued to care for her. He did this without comment or complaint.</p>
    </div>
    <div class="imgWrapper"></div>
    <div class="pWrapper">
        He was always a hard worker, and, leading by example, he instilled a rigid work ethic in all of us kids. My sister is now a successful registered nurse, my brother a successful certified public accountant, and myself – an accomplished tattoo artist. He helped us do well for ourselves as responsible citizens of society.  Life was good for my dad and mom — until Governor Whitmer’s shutdown happened, and things were never the same.
    </div>
</section>

<section>
    <h2 class="sectionTitle">THE TRAGEDY CONTINUED</h2>
    <div class="imgWrapper"></div>
    <div class="pWrapper">
        <p>
            Dad’s funeral was limited to ten visiting people. Absurdly, we were forced to remain in our cars at his burial. <strong>He earned a military funeral gun salute, which, sadly, was also restricted. This was all due to the state’s shutdown.</strong>  Finally, my Mom was now without her caretaker and husband. Because of her advanced stages of dementia, her house was sold, and she was moved into a nursing home facility. To this day, nearly a year later, our family has not been able to visit our Mother because of the ongoing, unnecessary restrictions by Governor Whitmer.  I wish I could look Governor Whitmer in the eyes and tell her exactly what her myopic leadership has done to me personally, and my family. It has hurt us immensely:
            <br/>
            <strong>My Dad died alone in a hospital, restricted from visitation from the people who loved him, for nearly a month. His funeral was limited to ten people, and the military honors Dad earned were denied. His wife, my Mom, was moved into a nursing home where she was denied visitation from any family or friends.</strong>
            <br/>
            This all happened in the wake of my business having been forced to close.  All of this is due to the heartless, totalitarian decisions made by Governor Gretchen Whitmer.</p>

