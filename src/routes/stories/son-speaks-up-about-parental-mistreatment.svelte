<section>
    <h2 class="sectionTitle">WHAT NEXT, GOVERNOR? </h2>
    <div class="pWrapper">
        <p>
            Sadly, and shockingly, we have all witnessed so many governors and politicians of our ever-dividing country, hypocritically, attending large political rallies or celebrations, all against their very own restrictions of large gatherings. We have seen them unmasked, violating their own mandates. We have witnessed them patronize the very businesses that they have shut down and deemed “non-essential.” And we all have heard about the “science” that has never been clearly defined or verified that supposedly justifies this dictatorial governing.  I genuinely believe, if this sad personal reality befell Governor Whitmer, she would quickly sidestep all of her very own restrictions to do what is naturally and fundamentally correct for the ones that she loves. Yet she denies us these very liberties.  As my father taught me, we take responsibility for our lives. And I am taking responsibility as a citizen of Michigan: we must tell our stories as business owners, family members, and concerned citizens.
            <br/>
            This Governor is hurting our state more than helping, and I will work hard to make sure she never has a second term in office. I know this is what my dad would want. The America he fought for, the community he lived in, and the people he cherished deserve better than Governor Whitmer.</p>


