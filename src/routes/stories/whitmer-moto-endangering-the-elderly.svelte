        The admissions director informed me, per new state orders all facilities like theirs were being forced to start accepting COVID patients. Why? Because the Governor said that they needed to be able to spread patients out so as to avoid overcrowding in the hospitals.  I was shocked and disgusted, but not surprised. For weeks now, Governor Whitmer had been executing this misguided policy of putting COVID-infected patients among our most vulnerable seniors. She knew of the wave of deaths that followed her decision to send COVID patients to nursing homes where it spread among the elderly like wildfire. Yet not only did she continue to allow it — she kept forcing others to comply.
    </p>
    <p>
        Keep in mind, gigantic “field hospitals” set up by the Army Corp of Engineers were being shut down without ever serving a single patient. Millions of taxpayer dollars were spent to create these facilities for the express purpose of providing care to COVID patients. But inexplicably Governor Whitmer thought nursing homes — underequipped, and with untrained staff — were a better destination.  I decided it was in the best interest of my mother for me to take a leave of absence from work and care for her myself, which was no easy task. The extra responsibility, the exhaustion, and the loss of income was the least of my worries. Unlike Michigan’s Governor, my mother’s wellbeing and health came first and foremost. It is a choice I will never regret.
    </p>
