        After a long desperate search to send her somewhere that did not accept COVID patients, I found one place that said that they did not take them. I was so relieved to find a place for her to safely recover, or so I thought.  The same day that she was to be transferred, I had a sneaking suspicion that I was being misled. I decided to call the facility once more and requested to speak with the nurse supervisor.  I asked, again, if they had any COVID patients at the time. I expressed my concern about my elderly mother not being exposed under any circumstances.
